package bean;

import java.util.ArrayList;
import java.sql.Date;
/**
 * 
 * @author Andreas
 *
 */
public class Package {
	
	private String packageName;
	private Date packageStartDate;
	private String packageDiscription;
	private Integer packagePrice;
	private String packageDeparture;
	private ArrayList<Flight> flight = new ArrayList<Flight>();
	private ArrayList<Hotel> hotel = new ArrayList<Hotel>();
	private ArrayList<Place> places = new ArrayList<Place>();
	
	public String getPackageName() {
		return packageName;
	}
	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}
	public Date getPackageStartDate() {
		return packageStartDate;
	}
	public void setPackageStartDate(Date packageStartDate) {
		this.packageStartDate = packageStartDate;
	}
	public String getPackageDiscription() {
		return packageDiscription;
	}
	public void setPackageDiscription(String packageDiscription) {
		this.packageDiscription = packageDiscription;
	}
	public Integer getPackagePrice() {
		return packagePrice;
	}
	public void setPackagePrice(Integer packagePrice) {
		this.packagePrice = packagePrice;
	}
	public String getPackageDeparture() {
		return packageDeparture;
	}
	public void setPackageDeparture(String packageDeparture) {
		this.packageDeparture = packageDeparture;
	}
	public ArrayList<Flight> getFlight() {
		return flight;
	}
	public void setFlight(ArrayList<Flight> flight) {
		this.flight = flight;
	}
	public ArrayList<Hotel> getHotel() {
		return hotel;
	}
	public void setHotel(ArrayList<Hotel> hotel) {
		this.hotel = hotel;
	}
	public ArrayList<Place> getPlaces() {
		return places;
	}
	public void setPlaces(ArrayList<Place> places) {
		this.places = places;
	}
}
